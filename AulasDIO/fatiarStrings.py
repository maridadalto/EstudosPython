#usado para retornar substrings, informando inicio (start), fim (stop) e passo (step)
#[start:stop[,step]]
nome = "Marina Dadalto Candido"

print(nome[5])#retorna a letra na posicao indicada
print(nome[:7])#retorna do inicio ate a posicao 6 (7-1)
print(nome[6:])#retorna a partir da posicao 6 ate o final
print(nome[7:20])#retorna da posicao 7 ate a 20
print(nome[7:20:2])#retorna da posicao 7 ate a 20 pulando de 2 em 2
print(nome[:])#retorna a string completa
print(nome[::-1])#retorna a string invertida
print(nome[-1])#retorna a Ãºltima letra
